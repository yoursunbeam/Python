import turtle
turtle.hideturtle()
turtle.fillcolor('green')
turtle.begin_fill()
turtle.right(45)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.end_fill()
turtle.setheading(0)
turtle.penup()
turtle.forward(140)
turtle.pendown()
turtle.fillcolor('yellow')
turtle.begin_fill()
turtle.right(45)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.end_fill()
turtle.done()
